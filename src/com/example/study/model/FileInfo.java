package com.example.study.model;

public class FileInfo {
	private String file_idx;
	private String file_originalname;
	private String file_path;
	private String file_uploader;
	
	public String getFile_idx() {
		return file_idx;
	}
	public void setFile_idx(String file_idx) {
		this.file_idx = file_idx;
	}
	public String getFile_originalname() {
		return file_originalname;
	}
	public void setFile_originalname(String file_originalname) {
		this.file_originalname = file_originalname;
	}
	public String getFile_path() {
		return file_path;
	}
	public void setFile_path(String file_path) {
		this.file_path = file_path;
	}
	public String getFile_uploader() {
		return file_uploader;
	}
	public void setFile_uploader(String file_uploader) {
		this.file_uploader = file_uploader;
	}
	
	
}

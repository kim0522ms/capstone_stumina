package com.example.study.model;

public class DetailInfo {
	private String category_idx;
	private String detail_idx;
	private String detail_name;
	
	public String getCategory_idx() {
		return category_idx;
	}
	public void setCategory_idx(String category_idx) {
		this.category_idx = category_idx;
	}
	public String getDetail_idx() {
		return detail_idx;
	}
	public void setDetail_idx(String detail_idx) {
		this.detail_idx = detail_idx;
	}
	public String getDetail_name() {
		return detail_name;
	}
	public void setDetail_name(String detail_name) {
		this.detail_name = detail_name;
	}
}

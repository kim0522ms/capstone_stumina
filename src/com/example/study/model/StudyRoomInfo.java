package com.example.study.model;

public class StudyRoomInfo {
	private String studyroom_idx;
	private String studyroom_location;
	private String area_idx;
	private String studyroom_name;
	private String studyroom_info;
	
	public String getStudyroom_info() {
		return studyroom_info;
	}
	public void setStudyroom_info(String studyroom_info) {
		this.studyroom_info = studyroom_info;
	}
	public String getStudyroom_idx() {
		return studyroom_idx;
	}
	public void setStudyroom_idx(String studyroom_idx) {
		this.studyroom_idx = studyroom_idx;
	}
	public String getStudyroom_name() {
		return studyroom_name;
	}
	public void setStudyroom_name(String studyroom_name) {
		this.studyroom_name = studyroom_name;
	}
	public String getStudyroom_location() {
		return studyroom_location;
	}
	public void setStudyroom_location(String studyroom_location) {
		this.studyroom_location = studyroom_location;
	}
	public String getArea_idx() {
		return area_idx;
	}
	public void setArea_idx(String area_idx) {
		this.area_idx = area_idx;
	}
}
